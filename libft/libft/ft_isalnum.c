#include "libft.h"

int	ft_isalnum(int p)
{
	if ((p >= 65 && p <= 90)
		|| (p >= 97 && p <= 122)
		|| (p >= 48 && p <= 57))
		return (1);
	return (0);
}

/*return 1 if p is alphanumeric or 0 if it doesn't*/