#include "libft.h"

int	ft_isalpha(int c)
{
	if ((c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}
/*If the parameter isn't an alphabet will return 0, otherwise, if is char*/
/*will return 1*/